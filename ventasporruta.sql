      FN3NEWSSTAND.TOWN = 'PERALES DEL RIO' OR
      FN3NEWSSTAND.TOWN = 'EL BERCIAL') AND
FN3NEWSSTAND.CODE = FN3CODIFICATION.NEWSSTAND
