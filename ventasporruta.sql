SELECT FN3PRINTINGDETAIL.DATEPRINT AS FECHA,
       FN3CODIFICATION.DISTRIBUTORROUTE AS RUTA,
       SUM(DECISION+INCREASEDEC) AS SERVICIO
FROM  FN3PRINTINGDETAIL, FN3CODIFICATION
WHERE
FN3PRINTINGDETAIL.HEADER = 'MARCA' AND
FN3PRINTINGDETAIL.PRODUCT = 'DIARIO' AND
FN3PRINTINGDETAIL.ORDERF = '1' AND
FN3PRINTINGDETAIL.DATEPRINT = TO_DATE('20020520','YYYYMMDD') AND
--FN3PRINTINGDETAIL.DATEPRINT > TO_DATE('20020519','YYYYMMDD') AND
(FN3CODIFICATION.DISTRIBUTORROUTE = 'S017' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'S019' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'S022' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'S023' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'S024' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'S025' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'S026' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'A001' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'A017' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'A018' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'A019' OR
FN3CODIFICATION.DISTRIBUTORROUTE = 'A022') AND
FN3PRINTINGDETAIL.NEWSSTAND = FN3CODIFICATION.NEWSSTAND
GROUP BY FN3CODIFICATION.DISTRIBUTORROUTE,FN3PRINTINGDETAIL.DATEPRINT;

